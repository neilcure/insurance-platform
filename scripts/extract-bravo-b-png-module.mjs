import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const svgPath = path.join(__dirname, "..", "app", "icon.svg");
const outPath = path.join(__dirname, "..", "lib", "bravo-b-png-base64.ts");

const s = fs.readFileSync(svgPath, "utf8");
const m = s.match(/href="data:image\/png;base64,([^"]+)"/);
if (!m) {
  console.error("Could not find embedded PNG in icon.svg");
  process.exit(1);
}
const b64 = m[1];

const contents = `/**
 * PNG embedded in \`app/icon.svg\` (BGII "B" mark).
 * Generated by \`scripts/extract-bravo-b-png-module.mjs\` — re-run after replacing icon.svg,
 * then \`node scripts/generate-bravo-favicon.mjs\` to refresh \`app/favicon.ico\` + \`public/favicon.ico\`.
 */
export const BRAVO_B_PNG_BASE64 = ${JSON.stringify(b64)};

export function bravoBPngDataUri(): string {
  return "data:image/png;base64," + BRAVO_B_PNG_BASE64;
}
`;

fs.writeFileSync(outPath, contents, "utf8");
console.log(`Wrote ${outPath} (${b64.length} base64 chars)`);
console.log("Tip: node scripts/generate-bravo-favicon.mjs");
